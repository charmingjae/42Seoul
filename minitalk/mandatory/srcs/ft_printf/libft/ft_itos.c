#include "libft.h"

char	*ft_itos(int i)
{
	char	*buf;

	buf = (char *)malloc(sizeof(char) * 2);
	if (!buf)
		return (NULL);
	if (i >= 97 && i <= 122)
	{
		buf[0] = (char)i;
		buf[1] = '\0';
	}
	return (buf);
}
