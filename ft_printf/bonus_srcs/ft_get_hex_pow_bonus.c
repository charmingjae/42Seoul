#include "../headers/ft_printf_bonus.h"

t_ull	ft_get_hex_pow(t_ull i)
{
	t_ull	a;
	t_ull	b;

	a = 1;
	b = 1;
	while (a <= i)
	{
		b *= 16;
		a++;
	}
	return (b);
}
